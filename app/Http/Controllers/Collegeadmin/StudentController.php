     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $col_code = Auth::user()->col_code;
        $ss = $request->input('ss');

        $students = Student::where('stu_col_code',$col_code)
            ->search($ss)
            ->paginate(10);
        return view('collegeadmin.student.index', compact('students', 'ss'));
    }

